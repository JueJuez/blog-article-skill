def format_note_with_prompt(content: str, author: str = "", url: str = "", tags: list = None, add_metadata: bool = True) -> str:
    """
    格式化笔记内容

    Args:
        content: 笔记内容
        author: 作者信息
        url: 原文链接
        tags: 标签列表
        add_metadata: 是否添加标签、作者、链接等元数据（默认True）

    Returns:
        格式化后的笔记内容
    """
    tags = tags or []

    parts = []

    if add_metadata:
        # 添加标签
        if tags:
            parts.append("# " + " #".join(tags))

        # 添加作者和链接
        if author or url:
            if url:
                parts.append(f"作者：{author} + [{url}]({url})" if author else f"[{url}]({url})")
            else:
                parts.append(f"作者：{author}")

    parts.append(content)

    return "\n\n".join(parts)
